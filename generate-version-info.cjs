const fs = require('fs');
const path = require('path');
const simpleGit = require('simple-git');

const git = simpleGit();
const packageJson = require('./package.json');

async function generateVersionInfo() {
    // Get Git Info
    const gitLog = await git.log();
    const branch = await git.revparse(['--abbrev-ref', 'HEAD']);
    const shortHash = await git.revparse(['--short', 'HEAD']);
    const versionInfo = {
        version: packageJson.version,
        git: {
            branch: branch.trim(),
            latestCommit: gitLog.latest.hash,
            shortHash: shortHash.trim(),
            message: gitLog.latest.message,
            date: gitLog.latest.date
        }
    };
    // Write "version.json" file for devops to use in the prod build
    fs.writeFileSync(path.join(__dirname, 'public', 'version.json'), JSON.stringify(versionInfo, null, 4));
    // Write the ".env" file for dev during development (and the TmFooter)
    const envContent = `# THIS FILE IN GENERATED BY THE "predev" SCRIPT
VITE_COMMIT_DATE=${gitLog.latest.date}
VITE_COMMIT_HASH=${shortHash.trim()}
VITE_BRANCH_NAME=${branch.trim()}
`;
    fs.writeFileSync('.env', envContent);
}

generateVersionInfo().catch(err => {
    console.error('Failed to generate version info:', err);
    process.exit(1);
});
